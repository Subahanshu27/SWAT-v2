/**
 * Create workflow-updates/<uuid>/ folders (+ optional manifest.json).
 *
 * Examples:
 *   # From comma-separated IDs
 *   node scripts/init-workflow-update-folders.mjs --ids id1,id2,id3
 *
 *   # From file (one UUID per line)
 *   node scripts/init-workflow-update-folders.mjs --ids-file ../workflow-updates/ids.txt
 *
 *   # Refresh manifest with prod workflow names
 *   node scripts/init-workflow-update-folders.mjs --ids-file ../workflow-updates/ids.txt --write-manifest
 */
import { mkdir, writeFile, readFile } from 'node:fs/promises';
import { resolve, join } from 'node:path';
import {
  UUID_RE,
  loadEnv,
  createWorkflowDb,
  fetchWorkflow,
} from './lib/workflow-update-lib.mjs';

loadEnv();

function usage(exitCode = 0) {
  console.log(`Usage: node scripts/init-workflow-update-folders.mjs [options]

Options:
  --dir <folder>       Target root (default: ../workflow-updates)
  --ids <id1,id2>      Comma-separated workflow UUIDs
  --ids-file <file>    One UUID per line (.txt)
  --write-manifest     Write/update manifest.json with expected_name from prod
  -h, --help
`);
  process.exit(exitCode);
}

function parseArgs(argv) {
  const opts = {
    dir: '../workflow-updates',
    ids: null,
    idsFile: null,
    writeManifest: false,
  };
  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    if (arg === '-h' || arg === '--help') usage(0);
    if (arg === '--write-manifest') {
      opts.writeManifest = true;
      continue;
    }
    if (arg === '--dir') {
      opts.dir = argv[++i];
      if (!opts.dir) usage(1);
      continue;
    }
    if (arg === '--ids') {
      opts.ids = argv[++i];
      if (!opts.ids) usage(1);
      continue;
    }
    if (arg === '--ids-file') {
      opts.idsFile = argv[++i];
      if (!opts.idsFile) usage(1);
      continue;
    }
    console.error(`Unknown option: ${arg}`);
    usage(1);
  }
  if (!opts.ids && !opts.idsFile) {
    console.error('Provide --ids or --ids-file');
    usage(1);
  }
  return opts;
}

async function loadIds(opts) {
  const raw = [];
  if (opts.ids) raw.push(...opts.ids.split(','));
  if (opts.idsFile) {
    const text = await readFile(resolve(process.cwd(), opts.idsFile), 'utf8');
    raw.push(...text.split(/\r?\n/));
  }
  const ids = [...new Set(raw.map((s) => s.trim()).filter(Boolean))];
  for (const id of ids) {
    if (!UUID_RE.test(id)) {
      throw new Error(`Invalid UUID: ${id}`);
    }
  }
  return ids;
}

const opts = parseArgs(process.argv.slice(2));
const rootDir = resolve(process.cwd(), opts.dir);
const ids = await loadIds(opts);

await mkdir(rootDir, { recursive: true });

/** @type {Array<{ workflow_id: string; expected_name: string | null; workflow_json: string; prompt: string }>} */
const manifestItems = [];
let sb = null;

if (opts.writeManifest) {
  sb = createWorkflowDb();
}

console.log(`Init workflow-update folders (${ids.length})`);
console.log(`Root: ${rootDir}\n`);

for (const id of ids) {
  const folder = join(rootDir, id);
  await mkdir(folder, { recursive: true });
  await writeFile(join(folder, '.gitkeep'), '', 'utf8');

  let name = null;
  if (sb) {
    try {
      const row = await fetchWorkflow(sb, id);
      name = row.name;
      console.log(`✓ ${id}  ${name}`);
    } catch (err) {
      console.warn(`✓ ${id}  (name lookup failed: ${err.message})`);
    }
  } else {
    console.log(`✓ ${id}`);
  }

  manifestItems.push({
    workflow_id: id,
    expected_name: name,
    workflow_json: `${id}/workflow.json`,
    prompt: `${id}/prompt.json`,
  });
}

if (opts.writeManifest) {
  const manifestPath = join(rootDir, 'manifest.json');
  const manifest = {
    _doc: 'Auto-generated by init-workflow-update-folders.mjs',
    items: manifestItems.map(({ workflow_id, expected_name, workflow_json, prompt }) => ({
      workflow_id,
      ...(expected_name ? { expected_name } : {}),
      workflow_json,
      prompt,
    })),
  };
  await writeFile(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`, 'utf8');
  console.log(`\nWrote ${manifestPath}`);
}

console.log('\nNext: drop Floyo exports in each folder, then run your batch command:');
console.log('  npm run workflow:batch -- <N> normalize --apply');
console.log('  npm run workflow:batch -- <N> verify');
console.log('  npm run workflow:batch -- <N> apply --yes');
